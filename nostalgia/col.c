#include <stdio.h>
#include <stdint.h>
uint8_t rgb[16*3] = {
0x00,0x00,0x00,
0x08,0x08,0x08,
0x20,0x10,0x08,
0x10,0x08,0x20,
0x20,0x18,0x18,
0x20,0x18,0x20,
0x31,0x20,0x20,
0x20,0x20,0x39,

0x20,0x20,0x41,
0x4A,0x29,0x20,
0x6A,0x39,0x18,
0x20,0x31,0x5A,
0x4A,0x39,0x4A,
0x5A,0x41,0x5A,
0x18,0x4A,0x8B,
0xFF,0xFF,0xFF
};
int main(void)
{
  int i;
  int r,g,b;

  for(i = 0; i < 16*3; i+=3){
    r = (rgb[i+2]+7)>>3;
    g = (rgb[i+1]+3)>>2;
    b = (rgb[i]+7)>>3;
    printf("$%04x,",(r<<11)|(b<<6)|g);
  }
  return 0;
}
